public class _10_MenggunakanEscapeCharacterPadaSuatuString {
    public static void main(String[] args) {
        //Menggunakan escape character
        String str1 = "POLBENG";
        String str2 = "polbeng";
        System.out.println(str1 + "\n" + str2);
        String str3 = "RPL";
        System.out.println(str1 + "\n" + str3);
    }
}
